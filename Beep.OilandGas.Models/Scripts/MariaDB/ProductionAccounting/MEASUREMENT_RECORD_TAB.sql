CREATE TABLE `MEASUREMENT_RECORD`
(
    `MEASUREMENT_ID` VARCHAR(40),
    `MEASUREMENT_DATETIME` DATETIME,
    `MEASUREMENT_METHOD` VARCHAR(255),
    `MEASUREMENT_STANDARD` VARCHAR(255),
    `GROSS_VOLUME` DECIMAL(18,2),
    `BSW` DECIMAL(18,2),
    `NET_VOLUME` DECIMAL(18,2),
    `TEMPERATURE` DECIMAL(18,2),
    `PRESSURE` DECIMAL(18,2),
    `API_GRAVITY` DECIMAL(18,2),
    `CRUDE_OIL_PROPERTIES_ID` VARCHAR(40),
    `ACCURACY` DECIMAL(18,2),
    `MEASUREMENT_DEVICE` VARCHAR(255),
    `OPERATOR` VARCHAR(255),
    `NOTES` VARCHAR(2000),
    `ACTIVE_IND` VARCHAR(1) DEFAULT 'Y',
    `PPDM_GUID` VARCHAR(40),
    `REMARK` VARCHAR(2000),
    `SOURCE` VARCHAR(40),
    `ROW_QUALITY` VARCHAR(40),
    `ROW_CREATED_BY` VARCHAR(30),
    `ROW_CREATED_DATE` DATETIME,
    `ROW_CHANGED_BY` VARCHAR(30),
    `ROW_CHANGED_DATE` DATETIME,
    `ROW_EFFECTIVE_DATE` DATETIME,
    `ROW_EXPIRY_DATE` DATETIME,
    `ROW_ID` VARCHAR(40)
);
