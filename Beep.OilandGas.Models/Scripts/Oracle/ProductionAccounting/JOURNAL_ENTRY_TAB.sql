CREATE TABLE "JOURNAL_ENTRY"
(
    "JOURNAL_ENTRY_ID" VARCHAR2(40) NOT NULL,
    "ENTRY_NUMBER" VARCHAR2(255),
    "ENTRY_DATE" DATE,
    "ENTRY_TYPE" VARCHAR2(50),
    "STATUS" VARCHAR2(50),
    "DESCRIPTION" VARCHAR2(2000),
    "REFERENCE_NUMBER" VARCHAR2(255),
    "SOURCE_MODULE" VARCHAR2(40),
    "TOTAL_DEBIT" NUMBER(18,2),
    "TOTAL_CREDIT" NUMBER(18,2),
    "ACTIVE_IND" VARCHAR2(1) DEFAULT 'Y',
    "PPDM_GUID" VARCHAR2(40),
    "REMARK" VARCHAR2(2000),
    "SOURCE" VARCHAR2(40),
    "ROW_QUALITY" VARCHAR2(40),
    "ROW_CREATED_BY" VARCHAR2(30),
    "ROW_CREATED_DATE" DATE,
    "ROW_CHANGED_BY" VARCHAR2(30),
    "ROW_CHANGED_DATE" DATE,
    "ROW_EFFECTIVE_DATE" DATE,
    "ROW_EXPIRY_DATE" DATE,
    "ROW_ID" VARCHAR2(40)
);
