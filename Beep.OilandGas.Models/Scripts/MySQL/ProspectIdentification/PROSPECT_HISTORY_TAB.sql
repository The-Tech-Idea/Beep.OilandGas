CREATE TABLE `PROSPECT_HISTORY`
(
    `PROSPECT_ID` VARCHAR(40),
    `HISTORY_SEQ_NO` DECIMAL(18,2) NOT NULL,
    `FIELD_NAME` VARCHAR(255),
    `OLD_VALUE` VARCHAR(255),
    `NEW_VALUE` VARCHAR(255),
    `CHANGE_REASON` VARCHAR(255),
    `CHANGED_BY` VARCHAR(30),
    `CHANGE_DATE` DATETIME,
    `DESCRIPTION` VARCHAR(2000),
    `ACTIVE_IND` VARCHAR(1) DEFAULT 'Y',
    `EFFECTIVE_DATE` DATETIME,
    `EXPIRY_DATE` DATETIME,
    `PPDM_GUID` VARCHAR(40),
    `REMARK` VARCHAR(2000),
    `SOURCE` VARCHAR(40),
    `ROW_CHANGED_BY` VARCHAR(30),
    `ROW_CHANGED_DATE` DATETIME,
    `ROW_CREATED_BY` VARCHAR(30),
    `ROW_CREATED_DATE` DATETIME,
    `ROW_EFFECTIVE_DATE` DATETIME,
    `ROW_EXPIRY_DATE` DATETIME,
    `ROW_QUALITY` VARCHAR(40)
);
