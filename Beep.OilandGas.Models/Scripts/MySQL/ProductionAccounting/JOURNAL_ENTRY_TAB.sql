CREATE TABLE `JOURNAL_ENTRY`
(
    `JOURNAL_ENTRY_ID` VARCHAR(40) NOT NULL,
    `ENTRY_NUMBER` VARCHAR(255),
    `ENTRY_DATE` DATETIME,
    `ENTRY_TYPE` VARCHAR(50),
    `STATUS` VARCHAR(50),
    `DESCRIPTION` VARCHAR(2000),
    `REFERENCE_NUMBER` VARCHAR(255),
    `SOURCE_MODULE` VARCHAR(40),
    `TOTAL_DEBIT` DECIMAL(18,2),
    `TOTAL_CREDIT` DECIMAL(18,2),
    `ACTIVE_IND` VARCHAR(1) DEFAULT 'Y',
    `PPDM_GUID` VARCHAR(40),
    `REMARK` VARCHAR(2000),
    `SOURCE` VARCHAR(40),
    `ROW_QUALITY` VARCHAR(40),
    `ROW_CREATED_BY` VARCHAR(30),
    `ROW_CREATED_DATE` DATETIME,
    `ROW_CHANGED_BY` VARCHAR(30),
    `ROW_CHANGED_DATE` DATETIME,
    `ROW_EFFECTIVE_DATE` DATETIME,
    `ROW_EXPIRY_DATE` DATETIME,
    `ROW_ID` VARCHAR(40)
);
